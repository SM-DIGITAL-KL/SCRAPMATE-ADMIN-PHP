	<script type="text/javascript">
    $(document).ready(function(){
        // Check if jQuery is loaded
        if (typeof jQuery === 'undefined') {
            console.error('jQuery is not loaded!');
            $(".res_alert").html('<div class="alert alert-danger"><strong>Error: jQuery is not loaded. Please refresh the page.</strong></div>');
            return;
        }
        
        $('#logInform').on('submit', function(e){
            e.preventDefault(); // Prevent form default submission
            var button_text = $(".signin_b").html();
            $(".signin_b").html('<i class="fa fa-circle-o-notch fa-spin"></i> loading...');
            $(".signin_b").prop( "disabled", true );
            $(".res_alert").html(''); // Clear previous alerts
            
            var username = $("#username").val();
            var password = $("#password").val();
            
            // Validate inputs
            if (!username || !password) {
                $(".signin_b").html(button_text);
                $(".signin_b").prop( "disabled", false);
                $(".res_alert").html('<div class="alert alert-danger alert-dismissible fade show"><button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="btn-close"></button><strong>Please enter both username and password!</strong></div>');
                return;
            }

            $.post("{{route('dologin')}}", {
                'email': username,
                'password': password,
                '_token': '{{csrf_token()}}'
            })
            .done(function(data){
                console.log('Login response:', data);
                if (data.msg == 'invalid') {
                    $(".signin_b").html(button_text);
                    $(".signin_b").prop( "disabled", false);
                    $(".res_alert").html('<div class="alert alert-danger alert-dismissible fade show"><button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="btn-close"></button><strong>Username or password wrong!</strong></div>');
                } else if(data.msg == 'success'){
                    $(".res_alert").html('<div class="alert alert-info alert-dismissible fade show"><button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="btn-close"></button><strong>You successfully logged in. Redirecting...</strong></div>');
                    setTimeout(function(){
                        location.reload();
                    }, 500);
                } else {
                    // Unknown response
                    $(".signin_b").html(button_text);
                    $(".signin_b").prop( "disabled", false);
                    $(".res_alert").html('<div class="alert alert-warning alert-dismissible fade show"><button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="btn-close"></button><strong>Unexpected response. Please try again.</strong></div>');
                }
            })
            .fail(function(xhr, status, error){
                console.error('Login AJAX error:', status, error, xhr);
                $(".signin_b").html(button_text);
                $(".signin_b").prop( "disabled", false);
                var errorMsg = 'Login failed. ';
                if (xhr.status === 0) {
                    errorMsg += 'Network error - please check your connection.';
                } else if (xhr.status === 419) {
                    errorMsg += 'Session expired. Please refresh the page and try again.';
                } else if (xhr.status === 500) {
                    errorMsg += 'Server error. Please check server logs.';
                } else {
                    errorMsg += 'Error: ' + (xhr.responseJSON?.message || error || 'Unknown error');
                }
                $(".res_alert").html('<div class="alert alert-danger alert-dismissible fade show"><button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="btn-close"></button><strong>' + errorMsg + '</strong></div>');
            });
        });
    });
  </script>

